package com.pratice.anny;

public class Add {
    public static void main(String[] args) {
       float a = 30F;
       float b = 40F;

       System.out.println("a="+a);
       System.out.println("b="+b);

       System.out.println("a+b="+(a+b));
       System.out.println("a-b="+(a-b));
       System.out.println("a*b="+(a*b));
       System.out.println("a/b="+(a/b));
       System.out.println("a%b="+(a%b));

    }
}
